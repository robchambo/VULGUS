# ── Computed fields ──────────────────────────────────────────────────

def compute_fields(entry):
    """Add auto-computed fields to a new entry."""
    word = entry['word']

    # Variants: convert pipe-separated string to list
    variants_raw = entry.get('variants', '')
    if isinstance(variants_raw, str):
        entry['variants'] = [v.strip() for v in variants_raw.split('|') if v.strip()] if variants_raw else []

    # Tags: ensure it's a list
    if isinstance(entry.get('tags'), str):
        entry['tags'] = [t.strip() for t in entry['tags'].split(',') if t.strip()]

    entry['length'] = len(word.replace(' ', ''))
    entry['is_single_word'] = ' ' not in word

    entry['part_of_speech'] = POS_MAP.get(entry.get('semantic_type', ''), 'interjection')

    # Definition — use provided definition (unlike migrate which derived from etymology)
    # Already set in the entry

    entry['first_attested'] = _parse_first_attested(entry.get('etymology_note', ''))
    entry['register'] = REGISTER_MAP.get(entry.get('severity', 1), 'informal')
    entry['family'] = None

    # Wordle eligibility
    entry['wordle_eligible'] = (
        entry['is_single_word']
        and entry['length'] in {4, 5, 6, 7}
        and entry.get('severity', 1) <= 3
        and entry.get('ship', 'YES') == 'YES'
    )

    entry['license'] = _derive_license(entry.get('source', ''))

    # Add review fields that existing entries have
    entry.setdefault('reviewer_1', '')
    entry.setdefault('reviewer_2', '')
    entry.setdefault('date_approved', '')

    return entry


# ── Merge ────────────────────────────────────────────────────────────

def main():
    if not os.path.exists(LEXICON):
        print(f'ERROR: {LEXICON} not found.')
        sys.exit(1)

    with open(LEXICON, encoding='utf-8') as f:
        data = json.load(f)

    max_id = max(w['id'] for w in data['words'])
    existing_words = {w['word'] for w in data['words']}

    added = 0
    skipped = 0
    for entry in NEW_WORDS:
        if entry['word'] in existing_words:
            print(f"  SKIP (duplicate): {entry['word']}")
            skipped += 1
            continue
        max_id += 1
        entry['id'] = max_id
        entry = compute_fields(entry)
        data['words'].append(entry)
        existing_words.add(entry['word'])
        added += 1

    data['word_count'] = len(data['words'])
    data['generated_at'] = datetime.now(timezone.utc).isoformat()

    with open(LEXICON, 'w', encoding='utf-8') as f:
        json.dump(data, f, indent=2, ensure_ascii=False)

    print(f'\nAdded {added}, skipped {skipped} duplicates. Total: {data["word_count"]}')

    # Theme stats
    theme_counts = {}
    for w in data['words']:
        for tag in w.get('tags', []):
            theme_counts[tag] = theme_counts.get(tag, 0) + 1

    print('\nTop themes:')
    for tag, count in sorted(theme_counts.items(), key=lambda x: -x[1])[:20]:
        print(f'  {tag}: {count}')

    wordle = sum(1 for w in data['words'] if w.get('wordle_eligible'))
    print(f'\nWordle-eligible: {wordle}')

    # Regenerate xlsx
    xlsx_script = os.path.join(REPO, 'scripts', 'build_xlsx.py')
    if os.path.exists(xlsx_script):
        print('\nRebuilding xlsx...')
        subprocess.run([sys.executable, xlsx_script], check=True)
        print('Done.')
    else:
        print(f'\nWARNING: {xlsx_script} not found, skipping xlsx rebuild.')


if __name__ == '__main__':
    main()
